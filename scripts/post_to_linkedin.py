def read_state():
    """Return ``{"entry_id": str, "posts": {lang: iso_ts}}``.

    Tolerates the legacy format (a bare entry id, from when posts were a single
    bilingual message): that entry is treated as fully posted in every language
    so we never re-blast an already-published entry.
    """
    try:
        with open(TRACKING_FILE, encoding="utf-8") as f:
            raw = f.read().strip()
    except FileNotFoundError:
        return {"entry_id": "", "posts": {}}
    if not raw:
        return {"entry_id": "", "posts": {}}
    try:
        data = json.loads(raw)
    except json.JSONDecodeError:
        data = None
    if isinstance(data, dict) and "entry_id" in data:
        posts = data.get("posts")
        return {"entry_id": data["entry_id"], "posts": posts if isinstance(posts, dict) else {}}
    # Legacy bare-id format → consider every language already posted.
    return {"entry_id": raw, "posts": {lang: "" for lang in LANGUAGE_SEQUENCE}}


def write_state(state):
    os.makedirs(os.path.dirname(TRACKING_FILE), exist_ok=True)
    with open(TRACKING_FILE, "w", encoding="utf-8") as f:
        f.write(json.dumps(state) + "\n")


