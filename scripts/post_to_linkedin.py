def refresh_access_token(refresh_token, client_id, client_secret):
    """Exchange a refresh token for a fresh access + refresh token pair."""
    data = urllib.parse.urlencode({
        "grant_type": "refresh_token",
        "refresh_token": refresh_token,
        "client_id": client_id,
        "client_secret": client_secret,
    }).encode("utf-8")

    req = urllib.request.Request(
        LINKEDIN_TOKEN_URL,
        data=data,
        headers={"Content-Type": "application/x-www-form-urlencoded"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            return json.loads(resp.read())
    except urllib.error.HTTPError as e:
        body = e.read().decode("utf-8", errors="replace")
        print(f"Token refresh error {e.code}: {body}", file=sys.stderr)
        return None


def post_to_linkedin(entry, lang, access_token, person_urn, commentary=None, llm_hashtags=None):
    post_text = build_post_text(entry, lang, commentary=commentary, llm_hashtags=llm_hashtags)
    article_url = _localized_url(entry["url"], lang) if entry["url"] else None

    content = {
        "shareCommentary": {"text": post_text},
        "shareMediaCategory": "ARTICLE" if article_url else "NONE",
    }
    if article_url:
        content["media"] = [{"status": "READY", "originalUrl": article_url}]

    payload = {
        "author": person_urn,
        "lifecycleState": "PUBLISHED",
        "specificContent": {"com.linkedin.ugc.ShareContent": content},
        "visibility": {"com.linkedin.ugc.MemberNetworkVisibility": "PUBLIC"},
    }

    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(
        LINKEDIN_API_URL,
        data=data,
        headers={
            "Authorization": f"Bearer {access_token}",
            "Content-Type": "application/json",
            "X-Restli-Protocol-Version": "2.0.0",
        },
        method="POST",
    )

    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            body = json.loads(resp.read())
            return body.get("id", "(no id returned)")
    except urllib.error.HTTPError as e:
        body = e.read().decode("utf-8", errors="replace")
        print(f"LinkedIn API error {e.code}: {body}", file=sys.stderr)
        sys.exit(1)


