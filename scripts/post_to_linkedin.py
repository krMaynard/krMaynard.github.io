def main():
    access_token = os.environ.get("LINKEDIN_ACCESS_TOKEN")
    person_urn = os.environ.get("LINKEDIN_PERSON_URN")

    if not access_token or not person_urn:
        print(
            "Error: LINKEDIN_ACCESS_TOKEN and LINKEDIN_PERSON_URN must be set.",
            file=sys.stderr,
        )
        sys.exit(1)

    # --- Parse blog and check for new entry ---
    with open(BLOG_FILE, encoding="utf-8") as f:
        html = f.read()

    entry = parse_latest_entry(html)
    if not entry:
        print("No blog entries found in blog.html.")
        return

    if not entry.get("id"):
        print("Error: The latest blog entry is missing an 'id' attribute.", file=sys.stderr)
        sys.exit(1)

    # --- Determine which language is next in the sequence ---
    state = read_state()
    if state.get("entry_id") != entry["id"]:
        state = {"entry_id": entry["id"], "posts": {}}
    posts = state["posts"]

    next_lang = next((lang for lang in LANGUAGE_SEQUENCE if lang not in posts), None)
    if next_lang is None:
        print(f"All languages already posted for {entry['id']}. Nothing to do.")
        return

    # --- Space the follow-up posts out by the configured interval ---
    try:
        interval = int(os.environ.get("LINKEDIN_POST_INTERVAL_MINUTES") or DEFAULT_POST_INTERVAL_MINUTES)
    except ValueError:
        print(
            "Warning: invalid LINKEDIN_POST_INTERVAL_MINUTES — falling back to "
            f"{DEFAULT_POST_INTERVAL_MINUTES} min.",
            file=sys.stderr,
        )
        interval = DEFAULT_POST_INTERVAL_MINUTES
    timestamps = []
    for ts in posts.values():
        try:
            timestamps.append(datetime.fromisoformat(ts))
        except (TypeError, ValueError):
            continue
    if timestamps:
        elapsed = (datetime.now(timezone.utc) - max(timestamps)).total_seconds() / 60
        if elapsed < interval - POST_INTERVAL_GRACE_MINUTES:
            print(
                f"Next language '{next_lang}' for {entry['id']} not due yet "
                f"({elapsed:.0f} min since last post; interval {interval} min)."
            )
            set_github_output("posted", "false")
            return

    # --- Optional: refresh the access token automatically (only when posting) ---
    refresh_token = os.environ.get("LINKEDIN_REFRESH_TOKEN")
    client_id = os.environ.get("LINKEDIN_CLIENT_ID")
    client_secret = os.environ.get("LINKEDIN_CLIENT_SECRET")
    gh_pat = os.environ.get("GH_PAT")
    gh_repo = os.environ.get("GITHUB_REPOSITORY")

    if refresh_token and client_id and client_secret and gh_pat and gh_repo:
        print("Refreshing LinkedIn access token…")
        tokens = refresh_access_token(refresh_token, client_id, client_secret)
        if tokens and tokens.get("access_token"):
            access_token = tokens["access_token"]
            update_github_secret(gh_repo, "LINKEDIN_ACCESS_TOKEN", access_token, gh_pat)
            new_refresh = tokens.get("refresh_token")
            if new_refresh:
                update_github_secret(gh_repo, "LINKEDIN_REFRESH_TOKEN", new_refresh, gh_pat)
        else:
            print("Warning: token refresh failed — falling back to existing token.", file=sys.stderr)
    else:
        missing = [
            k for k, v in {
                "LINKEDIN_REFRESH_TOKEN": refresh_token,
                "LINKEDIN_CLIENT_ID": client_id,
                "LINKEDIN_CLIENT_SECRET": client_secret,
                "GH_PAT": gh_pat,
                "GITHUB_REPOSITORY": gh_repo,
            }.items() if not v
        ]
        print(f"Token auto-refresh disabled (missing: {', '.join(missing)}).")

    # --- Rewrite with an LLM (required) ---
    provider = os.environ.get("LLM_PROVIDER", DEFAULT_LLM_PROVIDER).strip().lower()
    if provider == "anthropic":
        api_key = os.environ.get("ANTHROPIC_API_KEY")
        key_name = "ANTHROPIC_API_KEY"
    else:
        provider = "gemini"
        api_key = os.environ.get("GEMINI_API_KEY")
        key_name = "GEMINI_API_KEY"

    if not api_key:
        print(f"{key_name} not set — aborting.", file=sys.stderr)
        sys.exit(1)

    post_body = _extract_post_body(entry["url"])
    if post_body:
        print(f"Using full post body for the LLM ({len(post_body)} chars).")
    else:
        print("Full post body not found — falling back to blog.html summary.")
        post_body = entry["summary"]
    lang_name = LANGUAGES[next_lang]["name"]
    print(f"Generating {lang_name} LinkedIn commentary with {provider}…")
    commentary, llm_hashtags = generate_linkedin_commentary(
        entry["title"], post_body, api_key, next_lang, provider=provider
    )

    # --- Second expert pass: critique-then-revise for native fluency ---
    if commentary and _env_flag("FLUENCY_REVIEW", default=True):
        print(f"Running native-fluency review pass ({lang_name})…")
        commentary = review_commentary_fluency(
            entry["title"], commentary, api_key, next_lang, provider=provider
        )

    # --- Deterministic backstop: fix half-width ASCII punctuation in zh/ja ---
    if commentary and next_lang in ("zh", "ja"):
        commentary = _normalize_cjk_punctuation(commentary)

    # --- Post to LinkedIn ---
    print(f"Posting {entry['id']} ({lang_name}) — {entry['title']}")
    post_id = post_to_linkedin(
        entry, next_lang, access_token, person_urn,
        commentary=commentary, llm_hashtags=llm_hashtags,
    )
    print(f"Posted to LinkedIn: {post_id}")

    posts[next_lang] = datetime.now(timezone.utc).isoformat()
    state["posts"] = posts
    write_state(state)
    set_github_output("posted", "true")
    set_github_output("posted_lang", next_lang)
    set_github_output("posted_entry", entry["id"])


if __name__ == "__main__":
    main()
