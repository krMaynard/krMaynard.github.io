def _slugify_tag(tag):
    slug = "".join(c for c in tag if c.isalnum())
    return "#" + slug if slug else ""


class _PostBodyParser(HTMLParser):
    """Extracts paragraph text from <div class="post-body">."""

    def __init__(self):
        super().__init__()
        self._in_body = False
        self._depth = 0
        self._in_p = False
        self._current = ""
        self.paragraphs = []

    def handle_starttag(self, tag, attrs):
        attrs_dict = dict(attrs)
        if tag == "div" and "post-body" in attrs_dict.get("class", ""):
            self._in_body = True
            self._depth = 1
            return
        if not self._in_body:
            return
        if tag == "div":
            self._depth += 1
        elif tag == "p":
            self._in_p = True
            self._current = ""

    def handle_endtag(self, tag):
        if not self._in_body:
            return
        if tag == "div":
            self._depth -= 1
            if self._depth == 0:
                self._in_body = False
        elif tag == "p" and self._in_p:
            self._in_p = False
            para = self._current.strip()
            if para:
                self.paragraphs.append(para)

    def handle_data(self, data):
        if self._in_body and self._in_p:
            self._current += data


class _TitleParser(HTMLParser):
    """Extracts the text of the first <h1> (the post headline)."""

    def __init__(self):
        super().__init__()
        self._in_h1 = False
        self._done = False
        self.title = ""

    def handle_starttag(self, tag, attrs):
        if tag == "h1" and not self._done:
            self._in_h1 = True

    def handle_endtag(self, tag):
        if tag == "h1" and self._in_h1:
            self._in_h1 = False
            self._done = True

    def handle_data(self, data):
        if self._in_h1:
            self.title += data


def _read_local_html(url):
    """Map a same-origin post URL to its local file and return the HTML, or None."""
    if not url or not url.startswith(SITE_BASE_URL):
        return None
    rel_path = url[len(SITE_BASE_URL):].lstrip("/")
    if not rel_path.endswith(".html"):
        return None
    try:
        with open(rel_path, encoding="utf-8") as f:
            return f.read()
    except FileNotFoundError:
        return None


def _extract_post_body(url):
    """Return the post's paragraph text (from <div class="post-body">), or None."""
    html = _read_local_html(url)
    if html is None:
        return None
    parser = _PostBodyParser()
    parser.feed(html)
    return "\n\n".join(parser.paragraphs) if parser.paragraphs else None


def _extract_post_title(url):
    """Return the post's <h1> headline (localized to the page's language), or None."""
    html = _read_local_html(url)
    if html is None:
        return None
    parser = _TitleParser()
    parser.feed(html)
    title = parser.title.strip()
    return title or None


def _call_gemini(model, prompt, api_key):
    """Call the Gemini generateContent API for a given model. Returns text or raises."""
    payload = {"contents": [{"parts": [{"text": prompt}]}]}
    data = json.dumps(payload).encode("utf-8")
    url = (
        "https://generativelanguage.googleapis.com/v1beta/models/"
        f"{model}:generateContent?key={api_key}"
    )
    req = urllib.request.Request(
        url,
        data=data,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    with urllib.request.urlopen(req, timeout=30) as resp:
        body = json.loads(resp.read())
        return body["candidates"][0]["content"]["parts"][0]["text"].strip()


def _call_anthropic(model, prompt, api_key):
    """Call the Anthropic Messages API for a given model. Returns text or raises."""
    payload = {
        "model": model,
        "max_tokens": ANTHROPIC_MAX_TOKENS,
        "messages": [{"role": "user", "content": prompt}],
    }
    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(
        ANTHROPIC_API_URL,
        data=data,
        headers={
            "Content-Type": "application/json",
            "anthropic-version": ANTHROPIC_VERSION,
            "X-Api-Key": api_key,
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=60) as resp:
            body = json.loads(resp.read())
            for block in body.get("content", []):
                if block.get("type") == "text":
                    return block["text"].strip()
            raise ValueError("No text block in Anthropic response")
    except urllib.error.HTTPError as e:
        err_body = e.read().decode("utf-8", errors="replace")
        raise RuntimeError(f"Anthropic API error {e.code}: {err_body}") from e


def _parse_commentary_and_hashtags(text):
    """Split Gemini output into (commentary, hashtags_string).

    Looks for a line starting with 'HASHTAGS:' anywhere in the text (usually
    last). Returns the remainder as commentary and only well-formed '#Tag'
    tokens as a space-separated string.
    """
    lines = text.strip().splitlines()
    raw_hashtag_line = ""
    commentary_lines = []
    for line in lines:
        stripped = line.strip()
        normalized = stripped.replace("*", "")  # strip markdown bold e.g. **HASHTAGS:**
        if normalized.upper().startswith("HASHTAGS:"):
            raw_hashtag_line = normalized[len("HASHTAGS:"):].strip()
        else:
            commentary_lines.append(line)
    # Clean tokens: strip trailing punctuation, add # if missing, drop empties
    valid_tokens = []
    for t in raw_hashtag_line.split():
        token = t.rstrip(",.;!?").lstrip("#")
        if token:
            valid_tokens.append(f"#{token}")
    commentary = "\n".join(commentary_lines).strip()
    return commentary, " ".join(valid_tokens)


def _strip_trailing_hashtag_lines(text):
    """Drop trailing lines that consist solely of #hashtags (and whitespace).

    The fluency reviser is told to omit hashtags, but if it appends a bare
    ``#Tag1 #Tag2`` line (no ``HASHTAGS:`` label) it would otherwise survive and
    duplicate the first pass's tags. A line keeping any non-hashtag word is left
    untouched.
    """
    lines = text.rstrip().split("\n")
    while lines:
        tokens = lines[-1].split()
        if tokens and all(t.startswith("#") for t in tokens):
            lines.pop()
        else:
            break
    return "\n".join(lines).rstrip()


# ASCII sentence punctuation → its CJK full-width equivalent.
_FULLWIDTH_PUNCT = {",": "，", ".": "。", "!": "！", "?": "？", ":": "：", ";": "；"}
# Characters that count as "CJK context" for the preceding-char test: ideographs
# (+ Extension A) and Japanese kana, plus existing CJK punctuation.
_CJK_PUNCT = "，。！？：；、（）「」『』《》“”…—"


def _is_cjk(ch):
    o = ord(ch)
    return (
        0x4E00 <= o <= 0x9FFF      # CJK Unified Ideographs
        or 0x3400 <= o <= 0x4DBF   # CJK Extension A
        or 0x3040 <= o <= 0x30FF   # Hiragana + Katakana (Japanese)
        or ch in _CJK_PUNCT
    )


def _normalize_cjk_punctuation(text):
    """Convert ASCII , . ! ? : ; to their full-width form **only when directly
    preceded by a CJK character** (zh/ja).

    The adjacency rule leaves Latin/number contexts untouched — e.g. "v2.0",
    "Fable 5", "9:30" keep their ASCII punctuation — while fixing the model's
    half-width slips inside Chinese/Japanese sentences ("…修复了，但…")."""
    out = []
    for i, ch in enumerate(text):
        repl = _FULLWIDTH_PUNCT.get(ch)
        if repl and i > 0 and _is_cjk(text[i - 1]):
            out.append(repl)
        else:
            out.append(ch)
    return "".join(out)


def _build_commentary_prompt(title, content, lang):
    meta = LANGUAGES[lang]
    if lang == "en":
        lang_clause = "Write the post in English."
        tone_guidelines = (
            "- Open with a strong hook (one short sentence that stops the scroll)\n"
            "- Conversational first-person tone\n"
            "- 100–150 words\n"
        )
    else:
        lang_clause = (
            f"Write the post in {meta['name']}. Write naturally and fluently for a "
            f"native {meta['name']}-speaking professional audience — not a literal "
            "translation of the English source."
        )
        tone_guidelines = (
            f"- Natural, fluent {meta['name']} in a professional LinkedIn tone\n"
            "- Open with a strong hook that stops the scroll\n"
            "- Roughly 100–150 words (adjust naturally for the language)\n"
        )
    hashtag_lang_note = (
        "" if lang == "en"
        else f"- You may mix English and {meta['name']} hashtags\n"
    )
    punctuation_note = f"- {meta['punctuation']}\n" if meta.get("punctuation") else ""
    return (
        "You are writing a LinkedIn post for Kieran Maynard, a product manager "
        "specialising in AI, compliance, and content policy.\n\n"
        f"Based on the blog post below, write a single LinkedIn post. {lang_clause}\n\n"
        "Return ONLY the post body in exactly this format "
        "(no URL, no sign-off, no emojis):\n\n"
        "[commentary]\n\n"
        "HASHTAGS: #Tag1 #Tag2 #Tag3\n\n"
        "Guidelines for the commentary:\n"
        f"{tone_guidelines}"
        "- Short paragraphs separated by blank lines — LinkedIn formatting\n"
        f"{punctuation_note}"
        "- End with a brief question or observation to invite engagement\n\n"
        "Guidelines for HASHTAGS:\n"
        "- Choose 3–5 relevant LinkedIn hashtags that will maximise reach\n"
        "- Mix broad professional tags with topic-specific ones\n"
        f"{hashtag_lang_note}"
        "- No spaces within a tag (e.g. #ProductManagement not #Product Management)\n\n"
        f"Title: {title}\n\n"
        f"Blog post:\n{content}"
    )


def _run_llm(prompt, api_key, provider, purpose="completion"):
    """Call the configured provider, trying its fallback model(s) in order.

    For Gemini, tries GEMINI_PRIMARY_MODEL then GEMINI_FALLBACK_MODEL; for
    Anthropic, uses ANTHROPIC_MODEL. Returns the response text, or raises the
    last error if every attempt fails.
    """
    if provider == "anthropic":
        attempts = [("Claude", ANTHROPIC_MODEL, _call_anthropic)]
    else:
        attempts = [
            ("Gemini", GEMINI_PRIMARY_MODEL, _call_gemini),
            ("Gemini", GEMINI_FALLBACK_MODEL, _call_gemini),
        ]

    last_err = None
    for i, (label, model, call) in enumerate(attempts):
        try:
            text = call(model, prompt, api_key)
            print(f"{label} {purpose} generated ({model}).")
            return text
        except Exception as e:
            last_err = e
            next_step = "giving up" if i == len(attempts) - 1 else "trying fallback"
            print(f"{label} {model} error: {e} — {next_step}.", file=sys.stderr)

    raise last_err if last_err else RuntimeError("no LLM attempts configured")


def generate_linkedin_commentary(title, content, api_key, lang, provider=DEFAULT_LLM_PROVIDER):
    """Generate single-language LinkedIn commentary from the blog post content.

    Returns (None, "") if generation fails (caller uses the verbatim summary).

    Returns:
        (commentary_text, hashtags_string) — hashtags_string is space-separated
        '#Tag' tokens chosen by the model, or empty string on failure.
    """
    prompt = _build_commentary_prompt(title, content, lang)
    try:
        text = _run_llm(prompt, api_key, provider, purpose=f"{LANGUAGES[lang]['name']} draft")
    except Exception:
        print("Commentary generation failed — falling back to verbatim summary.", file=sys.stderr)
        return None, ""
    return _parse_commentary_and_hashtags(text)


def _build_critique_prompt(title, commentary, lang):
    meta = LANGUAGES[lang]
    return (
        f"You are a senior native {meta['name']} editor and LinkedIn copywriter, "
        "reviewing a draft LinkedIn post before it is published for Kieran Maynard, "
        "a product manager specialising in AI, compliance, and content policy.\n\n"
        f"Critique the draft below for natural, native-level {meta['name']} fluency "
        "and LinkedIn impact. Pay particular attention to:\n"
        f"- Phrasing that sounds translated, stilted, or unnatural to a native {meta['name']} reader\n"
        "- Word choice, idiom, grammar, and an appropriate professional register/tone\n"
        + (f"- Punctuation: {meta['punctuation']}\n" if meta.get("punctuation") else "")
        + "- Whether the opening hook is strong and the post reads smoothly on LinkedIn\n\n"
        "List the concrete edits you would make as short, specific bullet points. If "
        "the draft is already excellent, say so briefly. Do NOT rewrite the post — "
        "give only the critique.\n\n"
        f"Post title: {title}\n\n"
        f"Draft post:\n{commentary}"
    )


def _build_revision_prompt(title, commentary, critique, lang):
    meta = LANGUAGES[lang]
    return (
        f"You are a senior native {meta['name']} editor and LinkedIn copywriter.\n\n"
        f"Revise the draft LinkedIn post below, in {meta['name']}, applying the editor "
        "critique to maximise native-level fluency and LinkedIn impact. Preserve the "
        "original meaning, first-person voice, approximate length, and paragraph "
        "structure (short paragraphs separated by blank lines).\n\n"
        + (f"{meta['punctuation']}\n\n" if meta.get("punctuation") else "")
        + "Return ONLY the revised post body — no preamble, no commentary, no hashtags, "
        "and no surrounding quotation marks.\n\n"
        f"Post title: {title}\n\n"
        f"Draft post:\n{commentary}\n\n"
        f"Editor critique:\n{critique}"
    )


def review_commentary_fluency(title, commentary, api_key, lang, provider=DEFAULT_LLM_PROVIDER):
    """Critique-then-revise editorial pass to improve native fluency.

    Runs two LLM calls (a native-editor critique, then a revision that applies
    it). Returns the improved commentary, or the original draft unchanged if
    either call fails or the model returns something empty.
    """
    if not commentary:
        return commentary
    name = LANGUAGES[lang]["name"]
    try:
        critique = _run_llm(
            _build_critique_prompt(title, commentary, lang),
            api_key, provider, purpose=f"{name} critique",
        )
        if not critique.strip():
            print("Fluency critique was empty — keeping the original draft.", file=sys.stderr)
            return commentary
        revised = _run_llm(
            _build_revision_prompt(title, commentary, critique, lang),
            api_key, provider, purpose=f"{name} revision",
        )
    except Exception as e:
        print(f"Fluency review failed ({e}) — keeping the original draft.", file=sys.stderr)
        return commentary

    # Strip any HASHTAGS: line and trailing hashtag-only lines the reviser may
    # add, so we don't duplicate the first pass's tags.
    revised, _ = _parse_commentary_and_hashtags(revised)
    revised = _strip_trailing_hashtag_lines(revised).strip()
    return revised if revised else commentary


def _localized_url(url, lang):
    """Return the ``lang`` equivalent of an English site URL.

    English pages live at the root (``/blog/foo.html``); localized pages live
    under ``/ja/``, ``/zh/``, ``/ko/``. Falls back to the English URL when the
    localized page doesn't exist locally (or the URL is off-site).
    """
    if not url:
        return url
    prefix = LANGUAGES[lang]["url_prefix"]
    if not prefix or not url.startswith(SITE_BASE_URL + "/"):
        return url
    path = url[len(SITE_BASE_URL):]  # leading slash retained
    rel = (prefix + path).lstrip("/")
    if rel.endswith(".html") and not os.path.exists(rel):
        return url
    return SITE_BASE_URL + prefix + path


def build_post_text(entry, lang, commentary=None, llm_hashtags=None):
    if llm_hashtags:
        hashtags = llm_hashtags
    else:
        hashtags = " ".join(s for t in entry["tags"] if t for s in [_slugify_tag(t)] if s)
    default_url = SITE_BASE_URL + LANGUAGES[lang]["url_prefix"] + "/blog.html"
    localized_url = _localized_url(entry["url"], lang) if entry["url"] else None
    url_line = localized_url or default_url
    # Headline localized to the post's language (the localized page's <h1>),
    # falling back to the English listing title if it can't be read.
    headline = _extract_post_title(localized_url) or entry["title"]
    body = commentary if commentary else entry["summary"]

    parts = [headline, "", body, "", url_line]
    if hashtags:
        parts += ["", hashtags]
    text = "\n".join(parts)

    if len(text) <= MAX_POST_CHARS:
        return text

    # Trim body to fit (only needed if the LLM returns something unexpectedly long)
    overhead = (
        len(headline) + 2
        + 2 + len(url_line)
        + (2 + len(hashtags) if hashtags else 0)
        + 1  # ellipsis
    )
    budget = max(0, MAX_POST_CHARS - overhead)
    trimmed = body[:budget]
    # ja/zh don't separate words with spaces, so rsplit on a space could discard
    # most of the post — only trim to a word boundary for space-delimited langs.
    if lang in ("en", "ko") and " " in trimmed:
        trimmed = trimmed.rsplit(" ", 1)[0]
    parts[2] = trimmed + "…"
    return "\n".join(parts)


